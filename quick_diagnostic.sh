echo "[1] Бот запущен?"
systemctl is-active taxibot && echo "✓ Да" || echo "✗ Нет"
echo ""

echo "[2] Порт 8080 слушается?"
netstat -tuln | grep 8080 > /dev/null && echo "✓ Да" || echo "✗ Нет"
echo ""

echo "[3] Последние 15 строк логов:"
echo "────────────────────────────────────────────────────────────"
journalctl -u taxibot -n 15 --no-pager
echo ""

